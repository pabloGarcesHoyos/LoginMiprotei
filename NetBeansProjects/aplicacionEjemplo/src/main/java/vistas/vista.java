    private void txtguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtguardarActionPerformed
        String nombre = txtNombre.getText();
        String cedula = txtCedula.getText();
        String edad = txtEdad.getText();

        Persona persona = new Persona(nombre, cedula, edad);
         boolean respuesta = controlador.guardarPersona(persona);

        if(respuesta){
            JOptionPane.showMessageDialog(null,"Se guardo");
        }else{
            JOptionPane.showMessageDialog(null,"error al guardar");
        }        
        JOptionPane.showMessageDialog(null, "Hola mundo");


    }//GEN-LAST:event_txtguardarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      String cedula = txtCedula.getText();
      Persona persona=controlador.buscarPersona(cedula);
      if (persona != null){
          txtNombre.setText(persona.getNombre());
          txtEdad.setText(String.valueOf(persona.getEdad()));
      }else{
          JOptionPane.showMessageDialog(null, "La persona de " + "cedula" + cedula + "No se encuentra registrado ");
          
      }
    }//GEN-LAST:event_jButton1ActionPerformed

